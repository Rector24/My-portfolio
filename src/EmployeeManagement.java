    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            // TODO add your handling code here:

            DBConnection db = new DBConnection();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(EmployeeManagement.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(EmployeeManagement.class.getName()).log(Level.SEVERE, null, ex);
        }

        String employeeID = txtEmployeeID.getText();
        String firstname = txtFirstname.getText();
        String lastname = txtLastname.getText();
        String email = txtEmail.getText();
        String password = txtPassword.getText();
        String phone = txtPhone.getText();

        if(employeeID.isEmpty() || firstname.isEmpty() || lastname.isEmpty() || email.isEmpty() || password.isEmpty() || phone.isEmpty()){

            JOptionPane.showMessageDialog(this,
                "Please enter all fields",
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }

        else{

            DefaultTableModel model = (DefaultTableModel) tblEmployeeData.getModel();
            model.addRow(new Object[] {employeeID, firstname, lastname, email, password, phone});

            txtEmployeeID.setText("");
            txtFirstname.setText("");
            txtLastname.setText("");
            txtEmail.setText("");
            txtPassword.setText("");
            txtPhone.setText("");

        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int row = tblEmployeeData.getSelectedRow();

        if(row < 0){

            JOptionPane.showMessageDialog(this,
                "No row was selected",
                "Select row",
                JOptionPane.ERROR_MESSAGE);
        }
        else{

            DefaultTableModel model = (DefaultTableModel) tblEmployeeData.getModel();
            model.removeRow(row);

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

