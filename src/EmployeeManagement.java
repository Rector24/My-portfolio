    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblEmployeeData.getSelectedRow();

        if(selectedRow != -1){

            String employeeID = tblEmployeeData.getValueAt(selectedRow, 0).toString();
            String firstname = tblEmployeeData.getValueAt(selectedRow, 1).toString();
            String lastname = tblEmployeeData.getValueAt(selectedRow, 2).toString();
            String email = tblEmployeeData.getValueAt(selectedRow, 3).toString();
            String password = tblEmployeeData.getValueAt(selectedRow, 4).toString();
            String phone = tblEmployeeData.getValueAt(selectedRow, 5).toString();

            JOptionPane.showConfirmDialog(this,
                "Employee ID: " + employeeID + "\nName: " + firstname + "\nLastname: " + lastname + "\nEmail: " + email + "\nPassword: " + password + "\nPhone number: " + phone,
                "Employee details", JOptionPane.OK_OPTION);
        }
        else{

            JOptionPane.showMessageDialog(this,
                "Please select a row first",
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnViewActionPerformed

